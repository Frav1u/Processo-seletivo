n = int(input("insira um número papra verificar que ele peertence a sequencia Fibonacci"))

a, b = 0, 1

while a < n:
    a, b = b, a + b

if( a == n ):
    print("o número ",n, "pertence a sequencia Fibonacci")

else:
    print("o número ",n, "não pertence a sequencia Fibonacci")

